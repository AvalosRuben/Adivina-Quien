class Character:
    def __init__(self, id, name, glasses, skin_tone, hair_color, eye_color, sex, facial_hair, hat, image):
        self.id = id
        self.name = name
        self.glasses = glasses #bool
        self.skin_tone = skin_tone
        self.hair_color = hair_color
        self.eye_color = eye_color
        self.sex = sex
        self.facial_hair = facial_hair
        self.hat = hat #bool
        self.image = image